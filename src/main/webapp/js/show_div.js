function showEx(Num) {
if(Num.style.display == "block") Num.style.display = "none";
else Num.style.display = "block";
}


function showExD(Num,Num2) {
if(Num.style.display == "block") {
	Num.style.display = "none";
	Num2.style.display = "block";
}
else{ 
	Num.style.display = "block";
	Num2.style.display = "none";
}
}

